
const CACHE_NAME = "bankieren-v1";
const ASSETS = [
  "./",
  "./index.html",
  "./loading.html",
  "./overzicht.html",
  "./rekening.html",
  "./style.css",
  "./logo.png"
];

self.addEventListener("install", e => {
  e.waitUntil(
    caches.open(CACHE_NAME).then(cache => cache.addAll(ASSETS))
  );
});

self.addEventListener("fetch", e => {
  e.respondWith(
    caches.match(e.request).then(r => r || fetch(e.request))
  );
});
